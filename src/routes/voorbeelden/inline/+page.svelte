<h1>Inline HTML elementen</h1>
<p>
  HTML biedt veel opties voor inline elementen, voor alle inline elementen kun je kijken op het <a href="https://developer.mozilla.org/en-US/docs/Web/HTML/Element"
    >Mozilla Developer Network</a
  >.
</p>
<ul>
  <li>
    Voor <strong>Bold text</strong>, gebruik
    <code class="language-plaintext highlighter-rouge">&lt;strong&gt;</code>.
  </li>
  <li>
    Voor <em>Italian text</em>, gebruik
    <code class="language-plaintext highlighter-rouge">&lt;em&gt;</code>.
  </li>
  <li>
    Voor afkortingen, zoals <abbr title="HyperText Markup Language">HTML</abbr> gebruik je
    <code class="language-plaintext highlighter-rouge">&lt;abbr&gt;</code>, met een optioneel
    <code class="language-plaintext highlighter-rouge">titel</code> attribuut.
  </li>
  <li>
    Voor citaties, zoals <cite>â€” Mark Otto</cite>, gebruik je
    <code class="language-plaintext highlighter-rouge">&lt;cite&gt;</code>.
  </li>
  <li>
    Voor<del>verwijderde</del> tekst, gebruik je
    <code class="language-plaintext highlighter-rouge">&lt;del&gt;</code>
    en voor <ins>ingevoegde tekst</ins> gebruik je
    <code class="language-plaintext highlighter-rouge">&lt;ins&gt;</code>.
  </li>
  <li>
    Superscript <sup>text</sup> gebruikt
    <code class="language-plaintext highlighter-rouge">&lt;sup&gt;</code>
    en subscript <sub>text</sub> gebruikt
    <code class="language-plaintext highlighter-rouge">&lt;sub&gt;</code>.
  </li>
</ul>
